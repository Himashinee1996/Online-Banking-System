package com.bank.loan;

public class Loan {
	private String LoanID;
	private String LoanType;
	private String LoanAmount;
	private String Intrest;
	private String TotalLoan;
	private String LoanStart;
	private String LoanEnd;
	
	public String getLoanID() {
		return LoanID;
	}
	public void setLoanID(String loanID) {
		LoanID = loanID;
	}
	public String getLoanType() {
		return LoanType;
	}
	public void setLoanType(String loanType) {
		LoanType = loanType;
	}
	public String getLoanAmount() {
		return LoanAmount;
	}
	public void setLoanAmount(String loanAmount) {
		LoanAmount = loanAmount;
	}
	public String getIntrest() {
		return Intrest;
	}
	public void setIntrest(String intrest) {
		Intrest = intrest;
	}
	public String getTotalLoan() {
		return TotalLoan;
	}
	public void setTotalLoan(String totalLoan) {
		TotalLoan = totalLoan;
	}
	public String getLoanStart() {
		return LoanStart;
	}
	public void setLoanStart(String loanStart) {
		LoanStart = loanStart;
	}
	public String getLoanEnd() {
		return LoanEnd;
	}
	public void setLoanEnd(String loanEnd) {
		LoanEnd = loanEnd;
	}
	
	
}
